def get_article_prompt(category: str, title: str, source: str,
                       url: str, content: str) -> str:
    """
    Φτιάχνει το τελικό prompt για ένα συγκεκριμένο άρθρο.
    Συνδυάζει το βασικό template με την ειδική ενότητα της κατηγορίας.
    """
    special = SPECIAL_SECTIONS.get(category, "")
    return ARTICLE_PROMPT_TEMPLATE.format(
        category=category,
        title=title,
        source=source,
        url=url,
        content=content[:3000],  # Κρατάμε τα πρώτα 3000 χαρακτήρες
        special_section=special
    )
